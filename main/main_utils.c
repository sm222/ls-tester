int	ft_print_file(char *name)
{
	int		f;
	char	d[88];

	f = open(name, O_RDONLY);
	if (f == -1)
		return (1);
	while (read(f, d, 88) > 0)
		printf("%s", d);
	close(f);
	write(1, "\n", 1);
	return (0);
}

